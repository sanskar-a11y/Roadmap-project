function resource(key) {
  const item = resources[key] || resources.agents;
  return { ...item, key };
}

function makeTasks(spec, dayNumber) {
  const [date, chapter, title, tag, resourceKeys] = spec;
  const main = resourceKeys[0];
  const secondary = resourceKeys[1] || resourceKeys[0];
  const cycle = [main, secondary, "github", main, secondary, "interview"];
  return [
    `Watch the required lesson for ${title.toLowerCase()} and write five blunt notes, not a transcript.`,
    `Define the core terms for ${tag} in your own words and include one thing that can go wrong.`,
    `Complete one hands-on drill for day ${dayNumber}: build, test, measure, or explain the smallest real version.`,
    `Create proof: screenshot, code snippet, trace, eval row, or README update. No proof means no mastery.`,
    `Write one interview-style answer explaining what you learned, the tradeoff, and the failure mode.`,
  ].map((text, index) => ({
    text,
    resource: resource(cycle[index]),
  }));
}

function makeObjectives(spec) {
  const [, , title, tag] = spec;
  return [
    `Explain ${title.toLowerCase()} without reading notes.`,
    `Produce one artifact that proves hands-on ${tag} practice.`,
    "Pass the harsh skill test at 80% or higher.",
    "Log one weakness and one repair action.",
  ];
}

function makeSkillTest(spec, number) {
  const [, , title, tag] = spec;
  return [
    `In two sentences, explain ${title.toLowerCase()} to a hiring manager without buzzwords.`,
    `Name two failure modes for today's ${tag} work and how you would detect them.`,
    "What would you log or measure to prove this works outside a demo?",
    "Give one case where using an agent would be worse than a simpler workflow.",
    `Build or outline a tiny example that applies today's topic. What exact proof shows it worked?`,
    number % 3 === 0
      ? "Harsh check: what part of your answer would collapse if an interviewer asked for evidence?"
      : "Harsh check: what shortcut are you tempted to take, and why is it dangerous?",
  ];
}

function helpForDay(number) {
  if (number <= 8) return number % 2 === 0 ? "notesOnly" : "docsAllowed";
  if (number <= 24) return number % 4 === 0 ? "openBuild" : "docsAllowed";
  if (number <= 40) return number % 3 === 0 ? "guidedAI" : "notesOnly";
  if (number <= 52) return number % 4 === 0 ? "openBuild" : "docsAllowed";
  return number % 2 === 0 ? "noHelp" : "notesOnly";
}

export const days = daySpecs.map((spec, index) => {
  const number = index + 1;
  const [date, chapter, title, tag, resourceKeys, learning, practiceTool, proofRequired] = spec;
  const helpKey = helpForDay(number);
  const isBuildDay = tag === "projects" || title.toLowerCase().includes("build");
  return {
    number,
    date,
    title,
    chapter,
    tag,
    xp: isBuildDay ? 140 : 90,
    learning,
    practice: {
      tool: practiceTool,
      prompt: `Use ${practiceTool} to prove today's ${tag} skill with a small but real artifact.`,
    },
    proofRequired,
    helpAllowed: helpRules[helpKey],
    helpKey,
    passCriteria: "80% minimum. If you score C or Fail, add the day to the Repair Queue and retest before mastery.",
    resources: resourceKeys.map(resource),
    steps: makeTasks(spec, number),
    objectives: makeObjectives(spec),
    skillTest: makeSkillTest(spec, number),
  };
});

